/*
  ЛР8 (ЛР14). Высокоуровневое программирование.
  Индивидуальное задание — вариант 12.

  Задача: сформировать число из ЧЁТНЫХ двоичных разрядов значения с переключателей
  (биты 0, 2, 4, …), упакованных подряд, и вывести результат на светодиоды.
  То есть out[k] = sw[2*k].
  Пример: sw = 0b011_1011_1000 -> результат 0b01_0100 (= 20).

  Периферия варианта: переключатели (ввод, 0x01) + светодиоды (вывод, 0x02).
  Доступ к регистрам — через указатели на структуры из platform.h.
*/
#include "platform.h"

// Сборка числа из чётных битов: result[k] = value[2*k].
static uint32_t pack_even_bits(uint32_t value)
{
  uint32_t result = 0;
  uint32_t i = 0;
  while (value != 0u)
  {
    result |= (value & 1u) << i;  // младший (чётный) бит -> позиция i
    value >>= 2;                  // переходим к следующему чётному биту
    i++;
  }
  return result;
}

// Основной цикл пуст: вся работа выполняется в обработчике прерывания,
// который вызывается при изменении положения переключателей.
int main(int argc, char** argv)
{
  (void)argc;
  (void)argv;
  while (true)
  {
    // ожидание прерывания от переключателей
  }
}

// extern "C" — чтобы имя функции в объектном файле осталось int_handler
// (как ожидает startup.S при компоновке).
extern "C" void int_handler()
{
  uint32_t sw     = sw_ptr->value;      // чтение переключателей (0x01000000)
  uint32_t result = pack_even_bits(sw); // вычисление по варианту 12
  led_ptr->value  = result;             // вывод результата на светодиоды (0x02000000)
}
