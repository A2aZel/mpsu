/*
  ЛР8 (ЛР14). Высокоуровневое программирование.
  Индивидуальное задание — вариант 19.

  Задача: определить, сколько раз встречается "11" в двоичном представлении
  значения с переключателей (sw_i) без пересечений, и вывести результат
  на светодиоды.

  Периферия варианта: переключатели (ввод, 0x01) + светодиоды (вывод, 0x02).
  Доступ к регистрам — через указатели на структуры из platform.h.
*/
#include "platform.h"

// Подсчёт непересекающихся пар "11" в двоичной записи числа.
// Если два младших бита равны "11" — увеличиваем счётчик и сдвигаем на 2
// (чтобы пары не пересекались), иначе сдвигаем на 1.
static uint32_t count_pairs(uint32_t value)
{
  uint32_t count = 0;
  while (value != 0u)
  {
    if ((value & 0x3u) == 0x3u)
    {
      count++;
      value >>= 2;
    }
    else
    {
      value >>= 1;
    }
  }
  return count;
}

// Основной цикл пуст: вся работа выполняется в обработчике прерывания,
// который вызывается при изменении положения переключателей.
int main(int argc, char** argv)
{
  (void)argc;
  (void)argv;
  while (true)
  {
    // ожидание прерывания от переключателей
  }
}

// extern "C" — чтобы имя функции в объектном файле осталось int_handler
// (как ожидает startup.S при компоновке).
extern "C" void int_handler()
{
  uint32_t sw     = sw_ptr->value;     // чтение переключателей (0x01000000)
  uint32_t result = count_pairs(sw);   // вычисление по варианту 19
  led_ptr->value  = result;            // вывод результата на светодиоды (0x02000000)
}
